class OptionalDemo { 

   static void main(String[] args) { 
      // Example of an Integer using def 
      def a = 100; 
      println(a); 
		
      // Example of an float using def 
      def b = 100.10; 
      println(b); 
		
      // Example of an Double using def 
      def c = 100.101; 
      println(c);
		
      // Example of an String using def 
      def d = "HelloWorld"; 
      println(d); 
   } 
} 
/*
Optional typing can be a powerful utility during development, 
but can lead to problems in maintainability during the later 
stages of development when the code becomes too vast and complex.
*/